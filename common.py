import argparse
import os
import sys
import torch


class Defaults(object):
    GRPC_MAX_MSG_LENGTH = 1073741824
    SERVER_ADDRESS = "localhost:8080"


def check_create_dir(path):
    if not os.path.exists(path):
        os.makedirs(path)
        print("Created new directory: ")


def signal_handler_free_cuda(sig, frame):
    # clear cuda cache
    torch.cuda.empty_cache()
    print('You pressed Ctrl+C! Free CUDA and exit')
    sys.exit(0)


def boolean_string(s):
    """String to bool conversion"""
    if s not in {'False', 'True'}:
        raise ValueError('Not a valid boolean string')
    return s == 'True'


def add_project_specific_args(parent_parser):
    parser = parent_parser.add_argument_group("FedMixStyle")
    parser.add_argument("--net", type=str, default="resnet50")
    parser.add_argument("--pretrain", type=boolean_string, default=False)
    parser.add_argument("--upstream", type=boolean_string, default=False)
    parser.add_argument("--optimizer", type=str, default="sgd")
    parser.add_argument("--ckpt_path", type=str, default="")
    parser.add_argument("--fast_server_startup", default=False, type=boolean_string)
    parser.add_argument("--dataset", type=str, default="office31")
    parser.add_argument("--subdomain_id", type=int, default=0)
    parser.add_argument('--log_parameters', default=0, type=int)
    return parent_parser


def parse_client_config():
    parser = argparse.ArgumentParser()
    parser.add_argument('--root', type=str, default='', help='path to dataset')
    parser.add_argument(
        '--output-dir', type=str, default='', help='output directory'
    )
    parser.add_argument(
        '--resume',
        type=str,
        default='',
        help='checkpoint directory (from which the training resumes)'
    )
    parser.add_argument(
        '--seed',  # !
        type=int,
        default=-1,
        help='only positive value enables a fixed seed'
    )
    parser.add_argument(
        '--source-domains',
        type=str,
        nargs='+',
        help='source domains for DA/DG'
    )
    parser.add_argument(
        '--target-domains',
        type=str,
        nargs='+',
        help='target domains for DA/DG'
    )
    parser.add_argument(
        '--transforms', type=str, nargs='+', help='data augmentation methods'
    )
    parser.add_argument(
        '--config-file', type=str, default='', help='path to config file'
    )
    parser.add_argument(
        '--dataset-config-file',
        type=str,
        default='',
        help='path to config file for dataset setup'
    )
    parser.add_argument(
        '--trainer', type=str, default='', help='name of trainer'
    )
    parser.add_argument(
        '--backbone', type=str, default='', help='name of CNN backbone'
    )
    parser.add_argument('--head', type=str, default='', help='name of head')
    parser.add_argument(
        '--model-dir',
        type=str,
        default='',
        help='load model from this directory for eval-only mode'
    )
    parser.add_argument(
        '--load-epoch',
        type=int,
        default=150,
        help='load model weights at this epoch for evaluation'
    )
    parser.add_argument(
        'opts',
        default=None,
        nargs=argparse.REMAINDER,
        help='modify config options using the command-line'
    )
    args = parser.parse_args()
    return args


